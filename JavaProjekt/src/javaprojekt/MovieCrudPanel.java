    private void btnConfirmCreateMovieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmCreateMovieActionPerformed
        MoviesDataFactory mf=new MoviesDataFactory();
        try
        {
            if("".equals(txtNewMovieTitle1.getText()))
            {
                JOptionPane.showMessageDialog(null,"Please fill in the title");
            }
            else{
            mf.CreateMovie(txtNewMovieTitle1.getText(), txtDesc.getText(), txtProd.getText(), txtAct.getText(), txtDur.getText(), txtNewYear.getText());
            JOptionPane.showMessageDialog(null,"Successfully created a new movie!");
            initTable();
            }
        }catch(Exception ex)
        {

        }
    }//GEN-LAST:event_btnConfirmCreateMovieActionPerformed

    private void btnEditSelectedMovieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditSelectedMovieActionPerformed
        try{

            selectedRowIndex=tabelMovie.getSelectedRow();
            lblTitle.setText(movieTableModel.getTitleFromRowId(selectedRowIndex));
            txtDescription.setText(movieTableModel.getDescriptionFromRowId(selectedRowIndex));
            txtProducer.setText(movieTableModel.getProducerFromRowId(selectedRowIndex));
            txtActors.setText(movieTableModel.getActorsFromRowId(selectedRowIndex));
            txtDuration.setText(movieTableModel.getDurationFromRowId(selectedRowIndex));
            txtYear.setText(movieTableModel.getYearFromRowId(selectedRowIndex));
            btnConfirmChanges.show();
        }catch(Exception e)
        {

        }
    }//GEN-LAST:event_btnEditSelectedMovieActionPerformed

