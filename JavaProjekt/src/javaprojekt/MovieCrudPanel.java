    private void btnDeleteRowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteRowActionPerformed
        MoviesDataFactory mf=new MoviesDataFactory();
        selectedRowIndex=tabelMovie.getSelectedRow();
        try
        {
            int input=JOptionPane.showConfirmDialog(null,"Are you sure that you want to delete that movie? It will also delete the movie from the database.");
            switch(input)
            {
                case 0:
                System.out.println("Removing movie from the database");
                String title;
                title = movieTableModel.getTitleFromRowId(selectedRowIndex);
                movieTableModel.removeRow(selectedRowIndex);
                mf.DeleteSelectedMovieFromTheDatabase(title);
                JOptionPane.showMessageDialog(null,"Movie deleted from the database.");
                break;
                case 1:
                JOptionPane.showMessageDialog(null, "Canceled");
                break;
                case 2:
                JOptionPane.showMessageDialog(null, "Canceled");
                break;
                default:
                break;
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "No movie selected");
        }
    }//GEN-LAST:event_btnDeleteRowActionPerformed

